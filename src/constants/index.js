export const calculateSizes1 = (isSmall, isMobile, isTablet) => {
  return {
    deskScale: isSmall ? 1.5 : isMobile ? 2.1 : isTablet ? 2.55 : 3, // Increased scale 3 times
    deskPosition: isMobile ? [0.5, -2.5, 0] : [0.25, -3.5, 0], // Move up by reducing negative Y value
    cubePosition: isSmall
      ? [4.5, -4.4, 0]
      : isMobile
      ? [5.67, -4.7, 0]
      : isTablet
      ? [9, -5, 0]
      : [9, -5.5, 0],
    reactLogoPosition: isSmall
      ? [4, 4, 0] // Move to the right for "isSmall"
      : isMobile
      ? [5, 4, 0] // Move to the right for "isMobile"
      : isTablet
      ? [9, 4, 0] // Move to the right for "isTablet"
      : [12, 3, 0], // Keep the same for default desktop
    ringPosition: isSmall
      ? [-3, 2, 0] // Closer to the center for small screens
      : isMobile
      ? [-4, 3, 0] // Move closer to the center for mobile screens
      : isTablet
      ? [-7.8, 1.9, 0] // Keep it within view for tablets
      : [-11, 2, 0], // Default (desktop) position, not too far left

    targetPosition: isSmall
      ? [-5, -9.6, -10]
      : isMobile
      ? [-7, -10, -10]
      : isTablet
      ? [-10, -3, -10]
      : [-13, -13, -10],
  };
};

